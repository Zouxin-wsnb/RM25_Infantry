/**
 * @brief  使能SysTick中断
 * 将SysTick中断的频率设置为1000Hz
 */
void EnableSystickIT(void)
{
  uint32_t tick_ms = 1; // 1

  HAL_SYSTICK_CLKSourceConfig(SYSTICK_CLKSOURCE_HCLK_DIV8);

  uint32_t reload = SystemCoreClock / 8000 * tick_ms; // SystemCoreClock / 8000 * tick_ms;

  SysTick->CTRL |= SysTick_CTRL_TICKINT_Msk; // Enable SysTick Interrupt
  SysTick->LOAD = reload;
  SysTick->CTRL |= SysTick_CTRL_ENABLE_Msk; // Enable SysTick
}

/**
 * @brief  系统中断，1ms
 * 代码的主循环
 */
void SysTick_Handler(void)
{
  HAL_IncTick();
  // receive and update data
  // run control loop
  if (!monitor->Is_Robot_Init)
  {
    return;
  }

  dr16->Update();
  bmi088->update();

  chassisController->Run();
  gimbalController->Run();
  shooterController->Run();

  GMmotorHandler->sendControlData();
  boardConnectivity->BoardConnectivity_Send();

  monitor->Monitor_Run();
}

